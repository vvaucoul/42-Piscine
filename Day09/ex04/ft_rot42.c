char	*ft_rot42(char *str)
{
	int i;
	int decal;

	i = 0;
	while (str[i])
	{
		decal = 0;
		while (decal <= 42)
		{
			if (str[i] == 'Z')
				str[i] = 'A';
			if (str[i] == 'z')
				str[i] = 'a';
			++decal;
			str[i] += 1;
		}
		++i;
	}
	return (str);
}
